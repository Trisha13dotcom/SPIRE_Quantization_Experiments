"""Extracting the MFCC features and creating the dataset

"""

import numpy as np
import librosa

def extract_mfcc(audio_array, sample_rate=16000, n_mfcc=40, max_len=200): 
#each number in this vector is the dct coefficient describing the frequency energy shape of one window of the audio signal

# 1. Feature extraction - Outputs shape: (40, T)
    mfcc = librosa.feature.mfcc(y=audio_array, sr=sample_rate, n_mfcc=n_mfcc)

    # 2. Take the mean across the time axis (axis=1)
    # This collapses the 'T' dimension entirely, leaving a 1D array of shape (40,)
    mfcc_mean = np.mean(mfcc, axis=1)

    #Adding the delta and delta delta features into the feature vector
    delta = DeltaFeatures()
    mfcc_delta = delta.compute_delta(mfcc)
    mfcc_delta_mean = np.mean(mfcc_delta, axis=1)

    mfcc_delta2 = delta.compute_delta(mfcc_delta)

    mfcc_delta2_mean = np.mean(mfcc_delta2, axis=1)
    mfcc_mean = np.concatenate((mfcc_mean, mfcc_delta_mean, mfcc_delta2_mean), axis=0)


    #print("Feature Vector length:", len(mfcc_mean))

    return mfcc_mean


def build_features(dataset, label, sample_rate=16000):
    X, y = [], [] 
    X_1stFeat, X_2ndFeat= [], []
    for item in dataset:
        audio = item["audio"]["array"] #Extracts the numpy array of the amplitude values
        sr    = item["audio"]["sampling_rate"] #Extracts the sampling rate for the particular audio signal

        # Resampling if needed
        # If the sampling rate for two audio files is different, then the MFCC features for the audio files are not comparable
        if sr != sample_rate:
            audio = librosa.resample(audio, orig_sr=sr, target_sr=sample_rate)

        #Applying quantization
        audio = quantize(audio, scheme=1)

        feat = extract_mfcc(audio)
        X.append(feat)
        y.append(label)
        X_1stFeat.append(feat[0])
        X_2ndFeat.append(feat[1])
    return X, y, X_1stFeat, X_2ndFeat


# Calling the function to convert the audio signals into a dataset
X_hi, y_hi, X_1stFeat_hi, X_2ndFeat_hi = build_features(hi, label=0)   # 0 = Hindi
X_en, y_en, X_1stFeat_en, X_2ndFeat_en = build_features(en, label=1)   # 1 = English

X = np.array(X_hi + X_en)
