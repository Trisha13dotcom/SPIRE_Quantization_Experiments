# Separate normalized training data for Hindi and English models
X_train_hi_scaled = X_train_scaled[y_train == 0]
X_train_en_scaled = X_train_scaled[y_train == 1]

# Model building and training for Hindi
model_hi = GaussianMixture(
    n_components=1,        # number of Gaussian components
    covariance_type='full',
    random_state=42
)
